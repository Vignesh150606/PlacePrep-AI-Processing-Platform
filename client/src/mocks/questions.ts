import type { Question } from "@placeprep/shared";

function options(
  questionId: string,
  values: Array<{ label: string; text: string; isCorrect?: boolean }>,
) {
  return values.map((v, i) => ({
    id: `${questionId}-opt-${i}`,
    questionId,
    label: v.label,
    text: v.text,
    isCorrect: Boolean(v.isCorrect),
  }));
}

